var prefix = 'v',
    specialAttributes = [
        'pre',
        'text',
        'repeat',
        'partial',
        'with',
        'component',
        'component-id',
        'transition'
    ],
    config = module.exports = {

        async       : true,
        debug       : false,
        silent      : false,
        enterClass  : 'v-enter',
        leaveClass  : 'v-leave',
        attrs       : {},

        get prefix () {
            return prefix
        },
        set prefix (val) {
            prefix = val
            updatePrefix()
        }
        
    }

function updatePrefix () {
    specialAttributes.forEach(function (attr) {
        config.attrs[attr] = prefix + '-' + attr
    })
}

updatePrefix()